# simple calculator

def addition(num_1, num_2):
    return num_1 + num_2


def subtraction(num_1, num_2):
    return num_1 - num_2


def multiplication(num_1, num_2):
    return num_1 * num_2


def division(num_1, num_2):
    return num_1 / num_2


print(addition(2,5))
print(subtraction(50,80))
print(multiplication(20,8))
print(division(5,8))
