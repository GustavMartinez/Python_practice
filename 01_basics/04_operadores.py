# Operadores matemáticos


# Operación de suma con tipos de datos 'int' y 'float'
# Operador de suma "+"

print("Operación de suma: ")
print(5+2)
print(3.5+4.58)


# Operaciones utilizando variables:
variable_x = 54
variable_y = 3.15487

print(variable_x + variable_y)


# Operación de resta con tipos de datos 'int' y 'float'
# Operador de resta "-"

print("Operación de resta: ")
print(5-2)
print(3.258 - 584.695)

variable_x = 54
variable_y = 3.15487
print(variable_y - variable_x)


# Operación de multiplicación con tipos de datos 'int' y 'float'
# Operador de multiplicación "*"

print("Operación de multiplicación")
print(54*5)
print(5.89 * 21.4)

variable_x = 32
variable_y = 2.5487
print(variable_y * variable_x)


# Operación de división con tipos de datos 'int' y 'float'
# Operador de división "/"

print("Operación de división")
print(23/5)
print(4.7/1.4)

variable_x = 20
variable_y = 3.17
print(variable_y / variable_x)


# Operación de potenciación con tipos de datos 'int' y 'float'
# Operador de potenciación "**"

print("2 elevado a la 5 es:")
dos_elevado_a_5 = 2**5
print(dos_elevado_a_5)

print("3 Elevado a la 10, es: ")
tres_elevado_a_la_10 = 3**10
print(tres_elevado_a_la_10)


# Para obtener el entero de la división, se usa //. Así:

print("Floor Division: ")
